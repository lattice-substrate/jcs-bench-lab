func generateAPISummary(resultsDir, tablesDir string) error {
	path := filepath.Join(resultsDir, "latest-api-bench.txt")
	data, err := os.ReadFile(path)
	if err != nil {
		return err
	}

	type benchLine struct {
		name string
		nsOp float64
		bOp  float64
	}
	var lines []benchLine
	for _, line := range strings.Split(string(data), "\n") {
		if !strings.HasPrefix(line, "Benchmark") {
			continue
		}
		fields := strings.Fields(line)
		if len(fields) < 3 {
			continue
		}
		bl := benchLine{name: fields[0]}
		for i := 1; i < len(fields)-1; i++ {
			switch fields[i+1] {
			case "ns/op":
				bl.nsOp, _ = strconv.ParseFloat(fields[i], 64)
			case "B/op":
				bl.bOp, _ = strconv.ParseFloat(fields[i], 64)
			}
		}
		lines = append(lines, bl)
	}

	type benchGroup struct{ nsOps []float64 }
	groups := map[string]*benchGroup{}
	order := []string{}
	for _, l := range lines {
		if _, ok := groups[l.name]; !ok {
			groups[l.name] = &benchGroup{}
			order = append(order, l.name)
		}
		groups[l.name].nsOps = append(groups[l.name].nsOps, l.nsOp)
	}

	type summaryKey struct{ workload, mode string }
	summaryData := map[summaryKey]map[string]float64{}
	summaryOrder := []summaryKey{}

	for _, name := range order {
		g := groups[name]
		meanNS := mean(g.nsOps)
		parts := strings.SplitN(name, "/", 2)
		if len(parts) != 2 {
			continue
		}
		impl, mode, ok := parseBenchPrefix(parts[0])
		if !ok {
			continue
		}
		workload := trimCPUSuffix(parts[1])
		sk := summaryKey{workload: workload, mode: mode}
		if _, ok := summaryData[sk]; !ok {
			summaryData[sk] = map[string]float64{}
			summaryOrder = append(summaryOrder, sk)
		}
		summaryData[sk][impl] = meanNS
		_ = impl
	}

	// Only within-language pairs, canonicalize mode, summary workloads.
	withinPairs := []pair{
		{A: "schubfach", B: "json-canon", Label: "go:schubfach-vs-bd"},
		{A: "schubfach-rs", B: "json-canon-rs", Label: "rs:schubfach-vs-bd"},
	}

	var b strings.Builder
	b.WriteString("% Auto-generated by scripts/generate-paper-tables.go\n")
	b.WriteString("\\begin{table}[htbp]\n")
	b.WriteString("\\caption{API benchmark summary: within-language algorithm comparisons (canonicalize mode, mean of $n{=}10$).}\n")
	b.WriteString("\\label{tab:api-summary}\n")
	b.WriteString("\\centering\\small\n")
	b.WriteString("\\begin{tabular}{llrrr}\n")
	b.WriteString("\\toprule\n")
	b.WriteString("Pair & Workload & \\schubfach (ns/op) & \\burgerdybvig (ns/op) & Speedup \\\\\n")
	b.WriteString("\\midrule\n")

	for _, p := range withinPairs {
		for _, sk := range summaryOrder {
			if sk.mode != "canonicalize" {
				continue
			}
			if !summaryWorkloads[sk.workload] {
				continue
			}
			implA := summaryData[sk][p.A]
			implB := summaryData[sk][p.B]
			if implA == 0 || implB == 0 {
				continue
			}
			speedup := implB / implA
			if implB < implA {
				speedup = implA / implB
			}
			fmt.Fprintf(&b, "%s & %s & %.0f & %.0f & %.2f$\\times$ \\\\\n",
				escape(p.Label), escape(sk.workload), implA, implB, speedup)
		}
	}

	b.WriteString("\\bottomrule\n")
	b.WriteString("\\multicolumn{5}{l}{\\footnotesize Full results for all pairs, modes, and workloads in Appendix~\\ref{sec:appendix-tables}.} \\\\\n")
	b.WriteString("\\end{tabular}\n")
	b.WriteString("\\end{table}\n")

	return os.WriteFile(filepath.Join(tablesDir, "api-summary.tex"), []byte(b.String()), 0o644)
}

